
//How to fetch the millisecond according to universal time from a given Date object?

var dateToday = new Date('October 15, 1996 05:35:32:77 GMT+11:00');
console.log(dateToday)                         //it will show the defined date
console.log(dateToday.getMilliseconds());   // .getMilliseconds is keyword is used to fetch the milli seconds from given date
